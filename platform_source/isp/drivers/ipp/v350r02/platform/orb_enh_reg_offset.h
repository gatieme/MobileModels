#ifndef __ORB_ENH_REG_OFFSET_H__
#define __ORB_ENH_REG_OFFSET_H__

/******************************************************************************/
/*                     ORB_ENH Registers' Definitions                         */
/******************************************************************************/

#define ORB_ENH_ENABLE_CFG_REG     0x0  /* Top Config */
#define ORB_ENH_BLK_NUM_REG        0x4  /* Block number config */
#define ORB_ENH_BLK_SIZE_REG       0x8  /* Block size config */
#define ORB_ENH_INV_BLK_SIZE_Y_REG 0xC  /* Block inv_size_y config */
#define ORB_ENH_INV_BLK_SIZE_X_REG 0x10 /* Block inv_size_x config */
#define ORB_ENH_EXTEND_CFG_REG     0x14 /* Extend edge cfg */
#define ORB_ENH_HIST_CLIMIT_REG    0x18 /* HIST Climit cfg */
#define ORB_ENH_LUT_SCALE_REG      0x1C /* LUT scale cfg */

#endif // __ORB_ENH_REG_OFFSET_H__
